/** Detect browser language and map to a supported Language code */
export function detectBrowserLanguage(): Language {
  if (typeof navigator === 'undefined') return 'en';
  const langs = navigator.languages?.length ? navigator.languages : [navigator.language];
  for (const tag of langs) {
    const lower = tag.toLowerCase();
    if (lower.startsWith('zh')) return 'zh';
    if (lower.startsWith('ja')) return 'ja';
    if (lower.startsWith('ko')) return 'ko';
    if (lower.startsWith('de')) return 'de';
    if (lower.startsWith('en')) return 'en';
  }
  return 'en';
}

/** Get HTML lang attribute value for a given Language */
export function getHtmlLang(lang: Language): string {
  return SUPPORTED_LANGUAGES.find((l) => l.code === lang)?.htmlLang ?? 'en';
}

/** Safe accessor for bilingual {en, zh} objects — falls back to 'en' for unsupported langs */
export function t2(obj: { en: string; zh: string }, lang: Language): string {
  return lang === 'zh' ? obj.zh : obj.en;
}

